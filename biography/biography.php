        <h4>
          Early Childhood
        </h4>

        <p>
          I grew up in the suburbs of Boston, Massachusetts, USA, in a multi-ethnic family.
          My father emigrated from Mumbai, India, and my mother is Jewish-American.
        </p>

        <p>
          As a child, I had access to a large collection of classical music recordings, both Indian and Western.
          I spent long hours listening to many beloved instrumental and vocal masterpieces.
          Handel’s Messiah was my favorite above all.
          By singing along with the recording, I memorized the text and began to develop my sense of phrasing and vibrato.
        </p>

        <h4>
          School Years
        </h4>

        <p>
          At age 7, I began to study the violin; yet singing remained my most innate and heartfelt mode of musical expression.
          In my early teens, I started voice lessons – first with a local teacher in my home town of Andover, and later at the New England Conservatory Preparatory Division in Boston.
          In summers, I attended several programs for young poeple, including the Bel Canto Institute in New Paltz, NY, and Boston University Tanglewood Institute (BUTI).
          I also became fascinated by the art of violinmaking, and spent a summer as an apprentice violinmaker, constructing a violin from scratch with my own hands.
        </p>

        <h4>
          College
        </h4>

        <p>
          During my college years, I majored in vocal performance at the Indiana University Jacobs School of Music in Bloomington, IN.
          At IU, I performed comprimario roles in student productions of La Traviata and Lucia di Lammermoor.
          I pursued additional summertime training at the Ezio Pinza Council for American Singers of Opera (EPCASO) in Oderzo, Italy.
        </p>

        <p>
          As a lifelong vegetarian, I enjoyed shopping at the Bloomington Farmers' Market, where I befriended a Mennonite farmer.
          This farmer welcomed me into his home as one of the family, and on his farm as an occasional farm hand.
          I became an avid cyclist, commuting to school and the farm by bicycle, and once even cycling through the Italian Alps, from the Milan airport to the EPCASO program in Oderzo.
          I also played Ultimate Frisbee with the Indiana University team and had my first exposure to yoga, which I continue to practice today.
          In my senior year of college, I competed in the annual Indiana University Little 500 bicycle race.
        </p>

        <h4>
          Graduate School
        </h4>

        <p>
          After college, I moved to New York City, enrolling in graduate school at Mannes College.
          After earning a Master’s degree from Mannes, I enrolled in the opera program at the Yale School of Music, where I completed a second Master’s degree.
          At Yale, I performed my first lead roles in full opera productions: Don Ramiro in <i>La Cenerentola</i>, and Tebaldo in <i>I Capuleti e I Montecchi</i>.
          During my graduate school years, I spent summers at various programs for young artists and emerging professionals, including International Vocal Arts Institute (IVAI), Central City Opera, and Festival d'Aix-en-Provence in France, where I portrayed Dorvil in <i>La Scala di Seta</i>.
        </p>

        <h4>
          Emerging Artist
        </h4>

        <p>
          Returning to New York City after graduation, I joined former Mannes colleagues in LoftOpera, a young, innovative troupe based in Brooklyn.
          There, in April 2015, I performed the role of Gennaro in <i>Lucrezia Borgia</i>, for which I earned high praise in the New York Times and several other publications.
        </p>

        <p>
          One year later, I had the privilege to perform with Opera Australia, in the Sydney Opera House, as Nadir in Bizet’s <i>Les pêcheurs de perles</i>.
        </p>

        <p>
